     public void updateLabels() {
        if (order != null) {
            // Actualizar el t√≠tulo de la ventana con el distribuidor
            String distributor = order.getDistributor() != null ? order.getDistributor() : "Desconocido";
            setTitle("Pedido al distribuidor " + distributor);

            // Actualizar lblMedicineInfo
            String medicineInfo = String.format("%s unidades del %s %s",
                    order.getRequiredQuantity(),
                    order.getMedicineTypeName(),
                    order.getNameMedicine());
            lblMedicineInfo.setText(medicineInfo);

            // Actualizar lblAddressInfo
            lblAddressInfo.setText(order.getAddressInfo());
        }
    }

